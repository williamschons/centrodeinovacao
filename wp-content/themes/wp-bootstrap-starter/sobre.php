 <h3 style="text-align:center;">A inovação no HC</h3>

  	
<p>

  O Centro de Inovação Tecnológica – INOVAHC, criado em Outubro de 2014, tem como principal objetivo encorajar a inovação no âmbito do HCFMUSP, administrar a propriedade intelectual gerada nesse ambiente, e providenciar meios para promover a transferência de conhecimento científico, tecnológico e cultural ao setor produtivo público e privado, visando a melhoria da saúde do Estado e da Nação. Esse propósito geral está de acordo com as leis estadual e federal, que têm como motivação tornar a inovação tecnológica um componente estratégico de economia e desenvolvimento.


	</p>
	
<p>
